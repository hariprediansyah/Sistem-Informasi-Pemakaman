    private void loadData() {
        String sql = "SELECT " +
                 "r.id AS transaksi_id, " +
                 "r.tanggal_pemesanan, " +
                 "r.jumlah_bayar, " +
                 "j.nama_jenazah, " +
                 "l.nama_lokasi, " +
                 "b.kode_blok, " +
                 "p.nomor_petak, " +
                 "r.catatan " +
                 "FROM transaksi r " +
                 "INNER JOIN users u ON r.user_input = u.id " +
                 "INNER JOIN petak_makam p ON r.petak_id = p.id " +
                 "LEFT JOIN blok_makam b ON p.blok_id = b.id " +
                 "LEFT JOIN lokasi_makam l ON b.lokasi_id = l.id " +
                 "LEFT JOIN jenazah j ON r.jenazah_id = j.id " +
                 "WHERE 1=1 ";

    if (Session.getRole().equalsIgnoreCase("guest")) {
        sql += " AND r.user_input = " + String.valueOf(Session.getUser_id()) + " ";
    }

    String search = txtSearch.getText();

    if (!search.isEmpty()) {
        sql += " AND ( " +
               "CAST(r.tanggal_pemesanan AS CHAR) LIKE '%" + search + "%' OR " +
               "j.nama_jenazah LIKE '%" + search + "%' OR " +
               "CAST(r.jumlah_bayar AS CHAR) LIKE '%" + search + "%' OR " +
               "l.nama_lokasi LIKE '%" + search + "%' OR " +
               "b.kode_blok LIKE '%" + search + "%' OR " +
               "p.nomor_petak LIKE '%" + search + "%' OR " +
               "r.catatan LIKE '%" + search + "%' " +
               ") ";
    }

    sql += " ORDER BY r.id DESC";

    Object[] Baris = {
        "No",
        "Action",
        "Tanggal Transaksi",
        "Nama Jenazah",
        "Jumlah Bayar",
        "Lokasi Makam",
        "Blok",
        "Petak",
        "Catatan"
    };


    model = new DefaultTableModel(null, Baris);
    tblData.setModel(model);

    try {
        Statement stat = conn.createStatement();
        ResultSet hasil = stat.executeQuery(sql);
        int num = 1;
        while (hasil.next()) {
            String[] data = {
                Integer.toString(num),
                hasil.getString("transaksi_id"),
                hasil.getString("tanggal_pemesanan"),
                hasil.getString("nama_jenazah"),
                hasil.getString("jumlah_bayar"),
                hasil.getString("nama_lokasi"),
                hasil.getString("kode_blok"),
                hasil.getString("nomor_petak"),
                hasil.getString("catatan")
            };
            model.addRow(data);
            num++;
        }

        TableActionEvent actionEvent = new TableActionEvent() {
            @Override
            public void onEdit(int row) {
                DialogTransaksiAddEdit dialog = setupDialog();
                dialog.setData(Integer.parseInt(model.getValueAt(row, 1).toString()));
                dialog.setVisible(true);
            }

            @Override
            public void onDelete(int row) {
                int dialogButton = JOptionPane.YES_NO_OPTION;
                int dialogResult = JOptionPane.showConfirmDialog(null, "Konfirmasi hapus reservasi?", "Warning", dialogButton);
                if (dialogResult == JOptionPane.YES_OPTION) {
                    String kode = model.getValueAt(row, 1).toString();
                    String sqlPetak = "UPDATE petak_makam SET status = 'Kosong' WHERE id IN (SELECT petak_id FROM transaksi WHERE id = " + kode + ")";
                    String sqlJenazah = "UPDATE jenazah SET status_pemakaman = 0 WHERE id IN (SELECT jenazah_id FROM transaksi WHERE id = " + kode + ")";
                    String deleteSql = "DELETE FROM transaksi WHERE id = ?";
                    try {
                        PreparedStatement stat = conn.prepareStatement(deleteSql);
                        PreparedStatement statPetak = conn.prepareStatement(sqlPetak);
                        PreparedStatement statJenazah = conn.prepareStatement(sqlJenazah);
                        stat.setString(1, kode);
                        statPetak.executeUpdate();
                        statJenazah.executeUpdate(); 
                        stat.executeUpdate();                       
                        JOptionPane.showMessageDialog(null, "Transaksi Berhasil Dihapus");
                        loadData();
                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(null, "Transaksi Gagal Dihapus: " + e.getMessage());
                    }
                }
            }
        };

        tblData.getColumnModel().getColumn(1).setCellRenderer(new TableActionCellRender());
        tblData.getColumnModel().getColumn(1).setCellEditor(new TableActionCellEditor(actionEvent));

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Gagal memuat data: " + e.getMessage());
    }
}

    
